plt.ylabel('Performance (TeraFLOPS)', fontsize=14, fontweight='bold')
plt.xticks(matrix_dims, [str(d) for d in matrix_dims])

# Peak Annotation
peak_val = calculate_tflops(matrix_dims, data_s["Warptailing"])[-1]
