import matplotlib.pyplot as plt
import seaborn as sns
import numpy as np

# Theme and Font Setup
sns.set_theme(style="whitegrid")
plt.rcParams.update({'font.size': 12, 'font.weight': 'bold', 'axes.labelweight': 'bold'})

# Benchmark Data
sizes = np.array([256, 512, 1024, 2048, 4096, 8192, 16384])
x = [str(s) for s in sizes]

naive_s = np.array([171.50e-6, 972.98e-6, 7040.10e-6, 40604.76e-6, 296082.34e-6, 2444402.00e-6, 22368427.80e-6])
hand_written_s = np.array([134.04e-6, 350.35e-6, 1018.54e-6, 4419.68e-6, 37114.74e-6, 299730.25e-6, 2663913.65e-6])
cublas_sgemm_s = np.array([129.67e-6, 163.85e-6, 753.19e-6, 4587.54e-6, 35776.80e-6, 275095.86e-6, 2399587.84e-6])

# Calculate TFLOPS: (2 * N^3) / (time_in_seconds * 10^12)
ops = 2.0 * (sizes ** 3)
naive_tflops = ops / naive_s / 1e12
hand_written_tflops = ops / hand_written_s / 1e12
cublas_sgemm_tflops = ops / cublas_sgemm_s / 1e12

fig, ax = plt.subplots(figsize=(12, 8))

# Explicit Grid Setup
ax.grid(True, which='both', linestyle='--', linewidth=0.7, color='gray', alpha=0.5, zorder=0)

# Plotting
ax.plot(x, naive_tflops, 'r-o', label='Naive Kernel', linewidth=3, markersize=8, zorder=3)
ax.plot(x, hand_written_tflops, 's-', color='#ff9f4b', label='Hand-written (Double Buff)', linewidth=3, markersize=8, zorder=3)
ax.plot(x, cublas_sgemm_tflops, 'D-', color='#2a9d8f', label='cuBLAS SGEMM', linewidth=3, markersize=8, zorder=3)

# Annotation for Hand-written vs Naive TFLOPS at 2048 (Index 3)
idx_2k = 3
speedup = hand_written_tflops[idx_2k] / naive_tflops[idx_2k]
ax.annotate(f'Speedup: {speedup:.1f}x TFLOPS\n(Hand-written vs Naive)',
             xy=(x[idx_2k], hand_written_tflops[idx_2k]),
             xytext=(0.25, 0.65), textcoords='axes fraction',
             fontsize=13, fontweight='bold', color='black',
             bbox=dict(boxstyle="round,pad=0.5", fc="white", ec="red", lw=2),
             arrowprops=dict(arrowstyle="->", connectionstyle="arc3,rad=-0.2", color='black', lw=1.5))

# Formatting
ax.set_title("FP32 SGEMM: Performance Comparison (RTX 3050)", fontsize=18, pad=20)
ax.set_ylabel("Performance (TFLOPS)", fontsize=14)
ax.set_xlabel("Matrix Dimension (N x N)", fontsize=14)
ax.ticklabel_format(style='plain', axis='y')
ax.legend(frameon=True, shadow=True, facecolor='white', fontsize=12)

plt.tight_layout()
plt.show()