plt.tight_layout()
plt.show()

# --- PLOT 2: COMPUTE THROUGHPUT ---
def calculate_tflops(n_dims, times_s):
    n = np.array(n_dims)
    return (2 * n**3) / times_s / 1e12

plt.figure(figsize=(12, 8))
for i, (label, times) in enumerate(data_s.items()):
    tflops = calculate_tflops(matrix_dims, times)
    plt.plot(matrix_dims, tflops, color=colors[i], marker=markers[i], label=label, linewidth=3, markersize=9)

