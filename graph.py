plt.gca().yaxis.set_major_formatter(FuncFormatter(lambda y, _: '{:g}'.format(y)))

plt.xlabel('Matrix Dimension (N x N)', fontsize=14, fontweight='bold')
plt.ylabel('Time (Seconds)', fontsize=14, fontweight='bold')
